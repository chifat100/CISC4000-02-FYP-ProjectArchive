        function toggleTheme() {
            const body = document.body;
            const theme = body.getAttribute('data-theme');
            body.setAttribute('data-theme', theme === 'dark' ? 'light' : 'dark');
            
            const icon = document.querySelector('.theme-toggle i');
            icon.className = theme === 'dark' ? 'fas fa-sun' : 'fas fa-moon';
        }

        document.addEventListener('DOMContentLoaded', function() {
            const circles = document.querySelectorAll('.progress-ring-circle');
            circles.forEach(circle => {
                const percent = circle.closest('.stat-card').querySelector('.stat-percentage').textContent;
                circle.style.setProperty('--progress', parseInt(percent));
            });

            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.style.strokeDashoffset = 
                            calc(339.292 - (339.292 * parseInt(entry.target.closest('.stat-card')
                            .querySelector('.stat-percentage').textContent)) / 100);
                    }
                });
            }, { threshold: 0.5 });

            circles.forEach(circle => observer.observe(circle));
        });
    </script>
